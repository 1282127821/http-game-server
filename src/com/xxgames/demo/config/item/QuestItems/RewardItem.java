public class RewardItem {
    private int type;
    private int id;
    private int num;
    public void setType(int type) {
        this.type = type;
    }
    public int getType() {
        return type;
    }

    public void setId(int id) {
        this.id = id;
    }
    public int getId() {
        return id;
    }

    public void setNum(int num) {
        this.num = num;
    }
    public int getNum() {
        return num;
    }
}
